use strict;
use warnings;
use Data::Dumper;

my @z;
my %res;
my $cnt = 0;

for my $f (1001..9889) {
	push(@z, $f) if substr($f, 0, 1) == substr($f, 3, 1) and substr($f, 1, 1) == substr($f, 2, 1);
}

for my $r (sort @z) {
	for my $i (sort @z) {
		my $sum = $r - $i;
		next if $sum <= 0;
		next unless substr($r, 0, 1) == substr($i, 1, 1);
		next unless substr($r, 1, 1) == substr($i, 0, 1);
		$cnt++;
		printf("(%02d)\t$r - $i = %04d ", $cnt, $sum);
		print "#" for (1..int((($sum / 100)) + 1) / 2);
		print "\n";
		$res{$sum} = 1;
	}
}

print Dumper \%res;

